class DataTypes{
    public static void main(String args[]){
        int x; // means it can only store integer values 
        x = 5;
        System.out.println(x);
        x = 10;
        System.out.println(x);

        float f = 10.54f; // floating value (decimal value) and we use 'f' after vaule
        System.out.println(f);

        double db = 12.34; // double value -> decimal value
        System.out.println(db);

        db = 34; // it auto convert 
        System.out.println(db);

        boolean b = true; // only true,false
        System.out.println(b);

        char ch = 'a'; // character like '$','A','v','#' 
        System.out.println(ch);

        String s = "hello"; // string 
        System.out.println(s);

        
    }
}